# Conversión de millas a kilómetros.

millas = float(input("Ingrese un valor en millas: "))
kilometros = float(input("Ingrese un valor en kilómetros: "))

kilometros_a_millas = kilometros / 1.61
millas_a_kilometros = millas * 1.61

print("El valor de ", millas, " millas es: ", millas_a_kilometros, " kilómetros.")
print("El valor de ", kilometros, " kilómetros  es: ", kilometros_a_millas, " millas.")

